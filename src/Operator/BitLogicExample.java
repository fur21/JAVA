package Operator;

public class BitLogicExample {
    public static void main(String[] args) {
        System.out.println("45 & 24 = " + (45 & 25));
        System.out.println("45 | 24 = " + (45 | 25));
        System.out.println("45 ^ 24 = " + (45 ^ 25));
        System.out.println("~45 = " + (~45));
    }
}
